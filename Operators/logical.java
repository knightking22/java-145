import java.util.*;

public class logical {
    public static void main(String args[]) {
        
        //logical And 
        System.out.println( (3>2) && (5<0));   

         //Logical OR
         System.out.println( (3>2) || (5<0));  

          //Logical Not
          System.out.println( !(3>2));       
    }
    
}
